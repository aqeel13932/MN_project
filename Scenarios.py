def Construct_Scenario(Scenario):
    Scenario = Scenario.split(',')
    result = ""
    for part in Scenario:
        morning_or_night=""
        if part[0]=='M':
            morning_or_night="1"
        else:
            morning_or_night="0"
        result+=morning_or_night*int(part[1:])
    return result
